angulo1= int(input("Ingrese un angulo: "))
angulo2= int(input("Ingrese un angulo: "))
angulo3= int(input("Ingrese un angulo: "))

if angulo1 + angulo2 + angulo3 == 180 : 
   print( " es un triangulo ") 

else :
   print( " no es un triangulo ")
